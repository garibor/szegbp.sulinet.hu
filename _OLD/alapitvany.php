<p>Hosszú lenne felsorolni az alapítók névsorát, de szinte valamennyi akkori kolléga érezte, hogy milyen nagy szerepe lesz majd gimnáziumunk életében az alapítványnak, és csatlakozott az alapítókhoz. Az alapító okirat szerint az alapítvány mindenekelőtt támogatni kívánja az iskola korszerű pedagógiai erőfeszítéseit. Elsődleges célul tűzte ki az itt tanító tanárok szakmai fejlődéséhez szükséges tárgyi feltételek biztosítását, csakúgy, mint annak elérését, hogy az iskola tanulói ne szenvedjenek szükséget az oktatásfinanszírozás szűkössége következtében. A hátrányos helyzetű, de kiemelkedően tehetséges és a közösségi munkában is példamutató tanulók évről évre ösztöndíjat pályázhatnak meg.</p>

<p>Rendkívül fontosnak tekintette és tekinti ma is az alapítvány a szülők bevonását az iskola teljes tevékenységének fejlesztésébe, különös tekintettel a nyelvoktatás, a számítástechnikai képzés, a szabadidő hasznos eltöltésének feltételei, a diáksport, a tehetséggondozás és a diákok pályairányítása terén.</p>

<p>A sok év távlatában megszámlálhatatlan mindaz a támogatás, melyet az alapítvány nyújtott például a bel- és külföldi tanulmányutakhoz, táborokhoz vagy a nemzetközi diákcserék megvalósulásához.</p>

