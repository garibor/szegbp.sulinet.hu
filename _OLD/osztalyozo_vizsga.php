				A Szilágyi Erzsébet Gimnázium szabályzata az osztályozó vizsgáról
 
Jelen szabályzat a 11/1994. (VI. 8.) MKM rendelet 9. sz. melléklete alapján készült.
 
Osztályozó vizsgát bármelyik tanulónk tehet minden, az iskolánkban tanított tantárgyból, ha az igazgató engedélyezi.
Osztályozó vizsgát kell tennie annak a tanulónak, aki nem osztályozható.
Osztályozó vizsgát más iskola tanulója is tehet, amennyiben vendégtanulói jogviszonyt létesít iskolánkkal.
 
				A vizsga részei:
				<ul>
					<li>Írásbeli és szóbeli vagy gyakorlati – a tantárgytól függően.</li>
					<li>Az írásbelit a vizsgázó diák alapórás tanára javítja. Ha ő nem áll rendelkezésre, vagy a jelentkező nem tanulja az adott tantárgyat az iskolában, a munkaközösség-vezető javaslatára az igazgató-helyettes jelöl ki javító tanárt.</li>
					<li>Az írásbeli vizsga időtartama: 1 év anyagából 45 perc, 2 év anygából 90 perc.</li>
					<li>A szóbeli vizsgán a jelentkező tételt húz, majd 20 perc felkészülési idő után felel. A felelés időtartama nem haladhatja meg a 10 percet. Nyelvi felelet estén – akárcsak az érettségin – nincs készülési idő.</li>
					<li>A szóbeli vizsga bizottság előtt zajlik. A bizottság tagja két, az adott tantárgyból diplomával rendelkező tanár, és egy fő az iskolavezetésből. Olyan tárgy esetében, amelyből csak egy szaktanárunk van, a bizottság kéttagú.</li>
				</ul>
 
				A vizsga anyaga:
				<ul>
					<li>A Helyi Tantervben az adott évfolyamra meghatározott tananyag.</li>
					<li>Előrehozott vagy rendes érettségi céljából olyan tárgyból, amelyet a gyerek nem tanul az iskolában, a 9-12. évfolyam anyagából kell vizsgázni.</li>
					<li>Egy alkalommal legfeljebb két évfolyam anyagából lehet vizsgázni.</li>
				</ul>
				
				A vizsga időpontjai:
				<ul>
					<li>Egy tanévben két vizsgaidőszakban lehet osztályozó vizsgát tenni: január végén és augusztus végén, a pótvizsgák napján/ napjain. Az írásbelit is és a szóbelit is előre meghatározott és kihirdetett időpontban tartjuk, de a szóbeli időpontját a vizsgázó és tanára indokolt esetben átteheti más időpontra.</li>
					<li>A vizsgázó – rendkívüli esetben, pl., ha több tárgyból kell vizsgát tennie – kérelmezheti az igazgatónál a vizsgaidőszak rá vonatkozó meghosszabbítását.</li>
				</ul>
				A vizsga díja:
				Az osztályozó vizsga ingyenes. A vizsga csak akkor térítésköteles, ha valaki olyan tárgyból kíván vizsgázni, amelyet iskolánkban nem tanul.
 
 
 
				A jelentkezés módja:
				<ul>
					<li>A januári vizsgára a téli szünet előtti utolsó tanítási napig, az augusztusira július 15-ig kell jelentkezni. (Akadályoztatás esetén a jelentkezési lapot a nyári ügyeleti napokon, de legkésőbb az alakuló értekezlet napján is be lehet adni, a késedelem okának indoklásával.)</li>
					<li>A jelentkezést csak az erre rendszeresített jelentkezési lapon lehet leadni az igazgatóhelyettesnek. A jelentkezést az iskolatitkár iktatja.</li>
				</ul>
 				
				A vizsga eredménye:
				<ul>
			        <li>A vizsga eredményéről jegyzőkönyv készül.</li>
			        <li>A befejezett vizsga eredménye nem változtatható meg, bekerül a naplóba, a bizonyítványba és a törzslapba.</li>
			        <li>Ha a vizsgázó a vizsgát megszakítja, a következő vizsgaidőszakban jelentkezhet újra.</li>
        			<li>Ha a vizsgázó a vizsgát neki fel nem róható okból kénytelen megszakítani, igazolása bemutatásával kérelmezheti az igazgatónál a vizsga vizsgaidőszakon kívüli befejezését.</li>
				</ul>
 
				Sikeres osztályozó vizsga után a nagykorú tanuló, vagy kiskorú tanuló gondviselője kérelmezheti az igazgatónál, hogy továbbra is látogassa az adott tanórát.<br/>
 
 
