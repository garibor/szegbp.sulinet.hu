			Tudnivalók a történelem kisérettségiről (11. évfolyam) 
  
			Történelem kisérettségi
 
<p>A történelem kisérettségi célja az érettségire való felkészítés segítése: a korábban tanultak felelevenítése és a feladattípusok valamint a vizsgahelyzet gyakoroltatása. Szükségességét és hasznosságát a korábbi évek érettségi eredményei igazolják.<br/>
A vizsgán a 11. évfolyam tanulói és az őket tanító szaktanárok vesznek részt.</p>
 
<p><strong>1. A vizsga tartalma:</strong></p>
<p>A magyar és egyetemes történelemből a munkaközösség által kijelölt témakörök az ókortól 1849-ig és a kerettantervekben szereplő, a témakörökhöz kapcsolható lexikai ismeretek (lásd mellékletek). A vizsga tartalma egységes az évfolyam valamennyi csoportja számára. Mind az írásbeli feladatai, mind a szóbeli tételei a megadott témakörök anyagából készülnek. A témaköröket a szaktanárok a tanítási év első félévében az őszi szünet előtti utolsó tanítási napig a vizsgázók számára nyilvánosságra hozzák.</p>
 
<p><strong>2. A vizsga részei:</strong><br/>
<p>Írásbeli: az érettségi írásbeli tesztfeladatokkal egyező típusú feladatokból a munkaközösség által összeállított egységes feladatsor. A megoldáshoz 45 perc (egy tanóra) áll rendelkezésre.<br/>
Szóbeli: a szóbeli érettségi feladatokkal egyező típusú tétel kidolgozása, és szóban történő ismertetése. (Felkészülési idő: min. 30 perc, a tétel ismertetése max. 15 perc, fakultációs csoportban max. 20 perc). A szóbeli tételeket az egyes csoportok számára a csoportot tanító szaktanár állítja össze, a tételek konkrét címe és tartalma nem nyilvános.</p>
 
<p><strong>3. A vizsga értékelése:</strong></p>
Írásbeli: a feladatlapot a tanuló szaktanára javítja és duplán súlyozott osztályzattal értékeli, ami a tanuló 11. éves év végi osztályzatába beleszámít. <br/>
Szóbeli: a feleletet a tanuló szaktanára értékeli duplán súlyozott osztályzattal, ami a tanuló 11. éves év végi osztályzatába beleszámít. <br/>
Az értékelés szempontjai:<br/>
feladatmegértés 4 pont<br/>
tájékozódás térben és időben 6 pont<br/>
kommunikáció és szaknyelv 10 pont<br/>
források használata 12 pont<br/>
eseményeket alakító tényezők feltárása 18 pont<br/>
<strong>összesen 50 pont</strong><br/>
<p>Az írásbeli és szóbeli vizsga értékelése megegyezik az érettségi értékelésével:<br/>
80-100% jeles<br/>
60-79% jó<br/>
40-59% közepes<br/>
25-39% elégséges<br/>
0-24% elégtelen<br/>
</p>
<p><strong>4. A vizsga időpontja:</strong></p>
Írásbeli: a tavaszi szünet utáni első olyan tanítási nap, amikor mind a három csoportnak van órája. A pótdolgozat időpontja a következő héten ugyanaz a tanítási nap.<br/>
Szóbeli: a magyar nyelv és irodalom írásbeli érettségi vizsga napja.
 
<p><strong>I. melléklet: kisérettségi témakörök</strong></p>
 
<p><strong>1. Az ókor és kultúrája</strong></p>
Az athéni demokrácia működése a Kr.e. 5. században.<br/>
Julius Caesar egyeduralmi kísérlete.<br/>
A görög-római hitvilág.<br/>
Az antikvitás kiemelkedő kulturális emlékei. <br/>
A zsidó vallás fő jellemzői.<br/>
A kereszténység kialakulása és főbb tanításai.<br/>
 
<p><strong>2. A középkor</strong></p>
A hűbériség és a jobbágyság jellemzői.<br/>
Az uradalom és a mezőgazdasági technika.<br/>
A nyugati és a keleti kereszténység főbb jellemzői.<br/>
A középkori város és a céhes ipar.<br/>
Az iszlám vallás kialakulása és főbb tanításai. <br/>
A román és gótikus építészet; a reneszánsz kultúra. <br/>
 
<p><strong>3. A középkori magyar állam megteremtése és virágkora</strong></p>
A magyar nép eredete, vándorlása és a honfoglalás.<br/>
Géza fejedelemsége és I. (Szent) István államszervező tevékenysége. <br/>
A tatárjárás és az ország újjáépítése IV. Béla idején.<br/>
A középkori magyar állam megerősödése I. Károly idején.<br/>
Hunyadi Mátyás reformjai és külpolitikája.<br/>
 
<p><strong>4. Szellemi, társadalmi és politikai változások a kora újkorban (1492-1789)</strong></p>
A földrajzi felfedezések és a kapitalista gazdaság jellemzői.<br/>
A lutheri és kálvini reformáció. <br/>
A katolikus megújulás. <br/>
A barokk stílus jellemzői.<br/>
Az alkotmányos monarchia jellemzői Angliában.<br/>
A felvilágosodás eszmerendszere és főbb képviselői források alapján.<br/>
 
<p><strong>5. Magyarország a kora újkorban (1490-1790)</strong></p>
A mohácsi vész és az ország három részre szakadása. <br/>
A várháborúk (1541-1568).<br/>
Erdély sajátos etnikai és vallási helyzete.<br/>
A Rákóczi-szabadságharc okai, főbb eseményei és eredményei.<br/>
Demográfiai és etnikai változások a 18. században. <br/>
Mária Terézia és II. József reformjai.<br/>
 
<p><strong>6. A polgári átalakulás, a nemzetállamok és az imperializmus kora (1789-1849)</strong></p>
Az Emberi és polgári jogok nyilatkozatának alapkérdései.<br/>
A korszak főbb eszmeáramlatainak (liberalizmus, nacionalizmus, konzervativizmus és szocializmus) jellemzői. <br/>
Az első ipari forradalom legjelentősebb területei (könnyűipar, nehézipar, közlekedés), néhány találmánya és a gyáripar kezdetei. <br/>
 
<p><strong>7. A polgárosodás kezdetei és kibontakozása Magyarországon (1790-1849)</strong></p>
A reformkor fő kérdései, Széchenyi és Kossuth reformprogramja.<br/>
A pesti forradalom eseményei, az áprilisi törvények.<br/>
A szabadságharc főbb katonai és politikai eseményei.<br/>
 
 
